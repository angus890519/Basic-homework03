#include <stdio.h> //函示庫
#include <stdlib.h> //函示庫
int main(void)
{
   float height,m,weight,BMI; 
    printf("======該使用者的BMI資訊======\n"); 
    printf("您的身高為：");
    scanf("%f",&height); //輸入身高 
    printf("您的體重為：");
    scanf("%f",&weight); //輸入體重  
    printf("=============================\n");
    m=height/100; //1公尺=100公分 
    BMI=weight/(m*m); //BMI公式 
    printf("BMI = %.2f\n\n",BMI); //%[輸出最小寬度][.精度] 
 	system("pause");//暫停視窗    
 	return 0;//回傳值為0  
}
